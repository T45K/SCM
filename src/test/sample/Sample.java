class Sample {
    public static int max(final int a, final int b) {
        return a > b ? a : b;
    }

    public static long max(final long x, final long y) {
        return
                x > y
                        ? x
                        :
                        y
                ;

    }

    public static int min(final int a, final int b) {
        return a < b ? a : b;
    }
}
